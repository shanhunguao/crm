package com.ykhd.office.domain.req;

import java.math.BigDecimal;

import com.ykhd.office.domain.bean.common.PageCondition;

/**
 * 支付申请列表查询条件
 */
public class PaymentAppCondition extends PageCondition {

	private Integer creator; //申请人
	private Integer scheduler; //排期人
	private Integer state; //状态
	private String customerBrand; //客户品牌名称
	private String accountHolder; //收方名称
	private Integer dateType; // 1创建时间  2付款时间
	private String startTime;
	private String endTime;
	private String oaName; //公众号名称或微信号
	private Integer schedule;
	private BigDecimal amount; //付款金额
	private String payAccount; //付款账户
	private Integer makeFapiao; //0已开票  1未开票
	
	public Integer getCreator() {
		return creator;
	}
	public void setCreator(Integer creator) {
		this.creator = creator;
	}
	public Integer getScheduler() {
		return scheduler;
	}
	public void setScheduler(Integer scheduler) {
		this.scheduler = scheduler;
	}
	public String getCustomerBrand() {
		return customerBrand;
	}
	public void setCustomerBrand(String customerBrand) {
		this.customerBrand = customerBrand;
	}
	public String getAccountHolder() {
		return accountHolder;
	}
	public void setAccountHolder(String accountHolder) {
		this.accountHolder = accountHolder;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public Integer getDateType() {
		return dateType;
	}
	public void setDateType(Integer dateType) {
		this.dateType = dateType;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getOaName() {
		return oaName;
	}
	public void setOaName(String oaName) {
		this.oaName = oaName;
	}
	public Integer getSchedule() {
		return schedule;
	}
	public void setSchedule(Integer schedule) {
		this.schedule = schedule;
	}
	public BigDecimal getAmount() {
		return amount;
	}
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}
	public String getPayAccount() {
		return payAccount;
	}
	public void setPayAccount(String payAccount) {
		this.payAccount = payAccount;
	}
	public Integer getMakeFapiao() {
		return makeFapiao;
	}
	public void setMakeFapiao(Integer makeFapiao) {
		this.makeFapiao = makeFapiao;
	}
}

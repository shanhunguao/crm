package com.ykhd.office.domain.req;

import com.ykhd.office.domain.bean.common.PageCondition;

public class OASReturnCondition extends PageCondition {

	private Integer state; //状态
	private Integer schedule; //排期编号
	private Integer scheduler; //排期人
	private String oaName; //公众号名称
	private String customer; //客户公司名或品牌名
	private String accountHolder; //付方名称
	private Integer dateType; //1创建日期  2回款日期
	private String startTime;
	private String endTime;
	private Integer makeFapiao; //0已开票  1未开票
	
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public Integer getSchedule() {
		return schedule;
	}
	public void setSchedule(Integer schedule) {
		this.schedule = schedule;
	}
	public Integer getScheduler() {
		return scheduler;
	}
	public void setScheduler(Integer scheduler) {
		this.scheduler = scheduler;
	}
	public String getOaName() {
		return oaName;
	}
	public void setOaName(String oaName) {
		this.oaName = oaName;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getAccountHolder() {
		return accountHolder;
	}
	public void setAccountHolder(String accountHolder) {
		this.accountHolder = accountHolder;
	}
	public Integer getDateType() {
		return dateType;
	}
	public void setDateType(Integer dateType) {
		this.dateType = dateType;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public Integer getMakeFapiao() {
		return makeFapiao;
	}
	public void setMakeFapiao(Integer makeFapiao) {
		this.makeFapiao = makeFapiao;
	}
}

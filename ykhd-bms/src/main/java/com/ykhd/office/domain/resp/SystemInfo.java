package com.ykhd.office.domain.resp;

public class SystemInfo {

	private String name; //系统名称
	private String motto; //格言
	private Integer workingDay; //本月剩余工作日
	
	public SystemInfo(String name, String motto, Integer workingDay) {
		this.name = name;
		this.motto = motto;
		this.workingDay = workingDay;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMotto() {
		return motto;
	}
	public void setMotto(String motto) {
		this.motto = motto;
	}
	public Integer getWorkingDay() {
		return workingDay;
	}
	public void setWorkingDay(Integer workingDay) {
		this.workingDay = workingDay;
	} 
}
